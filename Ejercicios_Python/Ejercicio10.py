#Diferencias entre Python y JavaScript

#JavaScript
#Es un lenguaje de programación principalmente utilizado para crear páginas web interactivas. 
#Es el lenguaje "nativo" de los navegadores web y se ejecuta directamente en ellos.

#Python
#Es un lenguaje de programación de propósito general, utilizado en una amplia variedad de aplicaciones, 
#como desarrollo web, ciencia de datos, inteligencia artificial y automatización. 
#Es conocido por su sintaxis sencilla y fácil de leer.